void InterfaceUpdate()
{
  static uint8_t time_digits[3];
  static uint8_t faded_symb = 0;
  static char units[] = "m";
  if (number_fade)
  {
    faded_symb |= (1<<selected_symb);
    ClearArea(SSD1306.SelectedX, SSD1306.SelectedY, FONT_HEIGHT, FONT_WIDTH);
  }
  else
  {
    switch(selected_symb)
    {
    case 0:
      SSD1306.CurrentX = 74;
      SSD1306.CurrentY = 19;
      faded_symb &= ~(1<<selected_symb);
      draw_a_number(time_digits[selected_symb]);
      SSD1306.SelectedX = 74;
      SSD1306.SelectedY = 19;
      break;
    case 1:
      SSD1306.CurrentX = 94;
      SSD1306.CurrentY = 19;
      faded_symb &= ~(1<<selected_symb);
      draw_a_number(time_digits[selected_symb]);
      SSD1306.SelectedX = 94;
      SSD1306.SelectedY = 19;
      break;
    case 2:
      SSD1306.CurrentX = 117;
      SSD1306.CurrentY = 27;
      faded_symb &= ~(1<<selected_symb);
      draw_a_string(units);
      SSD1306.SelectedX = 117;
      SSD1306.SelectedY = 19;
      break;
    }
  }
  
  if(faded_symb)
  {
    faded_symb = 0;
    InterfaceDraw(time_digits[0], time_digits[1], units);
  }
  SSD1306_UpdateScreen();
}

void ClearArea(uint16_t x, uint16_t y, uint16_t height, uint16_t width)
{
  for (uint16_t i = x; i < x + width; i++)
  {
    for (uint16_t j = y; j < y + height; j++)
    {
      SSD1306_DrawPixel(i, j, 0);
    }
  }
}
